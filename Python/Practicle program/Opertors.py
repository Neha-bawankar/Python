# Arithmetic Operators
a = 10
b = 3

print("Arithmetic Operators")
print("a =", a)
print("b =", b)
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

# Comparison Operators
print("\nComparison Operators")
print("a =", a)
print("b =", b)
print("a == b =", a == b)
print("a != b =", a != b)
print("a > b =", a > b)
print("a < b =", a < b)
print("a >= b =", a >= b)
print("a <= b =", a <= b)

# Logical Operators
print("\nLogical Operators")
x = True
y = False
print("x =", x)
print("y =", y)
print("x and y =", x and y)
print("x or y =", x or y)
print("not x =", not x)

