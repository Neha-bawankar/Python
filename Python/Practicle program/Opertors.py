# Membership Operators
print("\nMembership Operators")
list1 = [1, 2, 3, 4, 5]
print("list1 =", list1)
print("2 in list1 =", 2 in list1)
print("6 in list1 =", 6 in list1)
print("2 not in list1 =", 2 not in list1)
print("6 not in list1 =", 6 not in list1)

# Identity Operators
print("\nIdentity Operators")
a = 10
