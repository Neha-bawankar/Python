# Integers
x = 10
print("num1:", x)
print("Type:", type(x))

# Floats
y = 3.14
print("num1:", y)
print("Type:", type(y))

# Complex numbers
z = 3 + 4j
print("Complex number:", z)
print("Type:", type(z))

# Strings
name = "John"
print("String:", name)
print("Type:", type(name))

# Boolean
is_admin = True
print("Boolean:", is_admin)
print("Type:", type(is_admin))

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits)
print("Type:", type(fruits))

# Tuple
colors = ("red", "green", "blue")
print("Tuple:", colors)
print("Type:", type(colors))

# Dictionary
person = {"name": "John", "age": 30, "city": "New York"}
print("Dictionary:", person)
print("Type:", type(person))

# Set
numbers = {1, 2, 3, 4, 5}
print("Set:", numbers)
print("Type:", type(numbers))

# Bytes
byte_string = b"Hello, World!"
print("Bytes:", byte_string)
print("Type:", type(byte_string))

# Bytearray
byte_array = bytearray(b"Hello, World!")
print("Bytearray:", byte_array)
print("Type:", type(byte_array))

# None
none_value = None
print("None:", none_value)
print("Type:", type(none_value))